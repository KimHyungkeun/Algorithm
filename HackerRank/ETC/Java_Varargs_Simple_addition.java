class Add {
    public void add(int n1, int n2) {
        int val = n1 + n2;
        System.out.println(n1 + "+" + n2 + "=" + val);
    }
    
    public void add(int n1, int n2, int n3) {
        int val = n1 + n2 + n3;
        System.out.println(n1 + "+" + n2 + "+" + n3 + "=" + val);
    }
    
    public void add(int n1, int n2 , int n3, int n4, int n5) {
        int val = n1 + n2 + n3 + n4 + n5;
        System.out.println(n1 + "+" + n2 + "+" + n3 + "+" + n4 + "+" + n5 + "=" + val);
    }
    
    public void add(int n1, int n2 , int n3, int n4, int n5, int n6) {
        int val = n1 + n2 + n3 + n4 + n5 + n6;
        System.out.println(n1 + "+" + n2 + "+" + n3 + "+" + n4 + "+" + n5 + "+" + n6 + "=" + val);
    }
}